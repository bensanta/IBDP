public class WhileLessonActivity1 {
    public static void main(String[] args) {
        //TODO: Print out numbers from 100 to 1 using DOâ€¦WHILE and WHILE loops
        System.out.println("Do... While:");
        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 100);

        System.out.println("While:");
        int j = 1;
        while (j <= 100){
            System.out.println(j);
            j++;
        }

    }
}
